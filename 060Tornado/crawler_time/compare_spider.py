def thread_():
    """
    多线程抓取
    采用Python内置的线程并发
    线程就是在一个进程中不断切换，让每个线程各自运行一会儿
    这对于网络io来说，性能是非常高的
    但是线程之间的切换比较浪费资源
    """
    from threading import Thread
    import requests
    import time
    urls = URLS
    headers = HEADERS
    headers['user-agent'] = "Mozilla/5.0+(Windows+NT+6.2;+WOW64)+AppleWebKit/537.36+(KHTML,+like+Gecko)+Chrome/45.0.2454.101+Safari/537.36"
    def get(url):
        try:
            r = requests.get(url, allow_redirects=False, timeout=2.0, headers=headers)
        except:
            pass
        else:
            print(r.status_code, r.url)

    print(u'多线程抓取')
    ts = [Thread(target=get, args=(url,)) for url in urls]
    t1 = time.time()
    for t in ts:
        t.start()
    for t in ts:
        t.join()
    print(time.time() - t1)


